# 各组总分
GROUP_TOTAL = {
    "线上实操": sum(c["max"] for c in ONLINE_CRITERIA.values()),
    "线下实操": sum(c["max"] for c in ONLINE_CRITERIA.values()),
    "线上答辩": sum(c["max"] for c in DEFENSE_ONLINE_CRITERIA.values()),
    "甘肃线下实操": sum(c["max"] for c in GANSU_CRITERIA.values()),
}


def get_criteria(group: str) -> dict:
    """获取指定裁判组的评分标准"""
    return GROUP_CRITERIA.get(group, {})


def get_total_score(group: str) -> int:
    """获取指定裁判组的满分"""
    return GROUP_TOTAL.get(group, 0)


def get_groups() -> list:
    """获取所有裁判组列表"""
    return list(GROUP_CRITERIA.keys())


def get_deductions(group: str) -> dict:
    """获取扣分项定义"""
    if group in ("线上实操", "线下实操"):
        return ONLINE_DEDUCTIONS
    if group == "甘肃线下实操":
        return GANSU_DEDUCTIONS
    return {}


def get_veto(group: str) -> dict:
    """获取否决项定义"""
    if group in ("线上实操", "线下实操"):
        return ONLINE_VETO
    if group == "甘肃线下实操":
        return GANSU_VETO
    return {}

